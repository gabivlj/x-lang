func BenchmarkCompilerScopes(t *testing.B) {
	c := New()
	if c.scopeIndex != 0 {
		t.Errorf("scopeIndex wrong. got=%d, want=%d", c.scopeIndex, 0)
	}

	c.emit(code.OpMul)

	c.enterScope()
	if c.scopeIndex != 1 {
		t.Errorf("scopeIndex wrong. got=%d, want=%d", c.scopeIndex, 1)
	}

	c.emit(code.OpSub)

	if len(c.scopes[c.scopeIndex].instructions) != 1 {
		t.Errorf("instructions length wrong. got=%d",
			len(c.scopes[c.scopeIndex].instructions))
	}

	last := c.scopes[c.scopeIndex].lastInstruction
	if last.Opcode != code.OpSub {
		t.Errorf("lastInstruction.Opcode wrong. got=%d, want=%d",
			last.Opcode, code.OpSub)
	}

	c.leaveScope()
	if c.scopeIndex != 0 {
		t.Errorf("scopeIndex wrong. got=%d, want=%d",
			c.scopeIndex, 0)
	}

	c.emit(code.OpAdd)

	if len(c.scopes[c.scopeIndex].instructions) != 2 {
		t.Errorf("instructions length wrong. got=%d",
			len(c.scopes[c.scopeIndex].instructions))
	}

	last = c.scopes[c.scopeIndex].lastInstruction
	if last.Opcode != code.OpAdd {
		t.Errorf("lastInstruction.Opcode wrong. got=%d, want=%d",
			last.Opcode, code.OpAdd)
	}

	previous := c.scopes[c.scopeIndex].previousInstruction
	if previous.Opcode != code.OpMul {
		t.Errorf("previousInstruction.Opcode wrong. got=%d, want=%d",
			previous.Opcode, code.OpMul)
	}
}

